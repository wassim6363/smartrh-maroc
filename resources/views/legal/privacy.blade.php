<h1>Politique de confidentialité</h1>
<p>SmartRH Maroc s'engage à protéger la vie privée des utilisateurs de sa plateforme SaaS de gestion RH et paie.</p>
<h2>Données collectées</h2>
<p>Nous collectons les données nécessaires à la gestion des ressources humaines : identité des salariés, coordonnées, informations contractuelles, données de paie et documents RH.</p>
<h2>Finalités</h2>
<p>Les données sont utilisées exclusivement pour le fonctionnement de la plateforme, le calcul de la paie, la génération de documents, le portail salarié et les obligations légales de l'employeur.</p>
<h2>Hébergement et sécurité</h2>
<p>Les données sont hébergées sur des serveurs sécurisés. L'accès est protégé par authentification forte, isolation par société et journalisation des accès.</p>
<h2>Conservation</h2>
<p>Les données sont conservées pendant la durée de la relation contractuelle, puis archivées conformément aux obligations légales marocaines.</p>
<h2>Vos droits</h2>
<p>Conformément à la loi 09-08, vous disposez d'un droit d'accès, de rectification et d'opposition au traitement de vos données personnelles.</p>
<h2>Contact</h2>
<p>Pour toute question relative à vos données, contactez-nous via notre formulaire de demande ou à l'adresse email indiquée sur le site.</p>
<p style="margin-top:24px;font-size:14px;color:#64748b;">Les paramètres de paie, modèles de contrats et documents générés doivent être vérifiés par un expert-comptable marocain, juriste ou professionnel compétent avant utilisation officielle.</p>
